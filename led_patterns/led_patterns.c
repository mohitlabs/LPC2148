#include<LPC21xx.h>
void delay(int);
unsigned int pattern[]={
	0x00800000,0x00400000,0x00200000,0x00100000,
	0x00080000,0x00040000,0x00020000,0x00010000,
	0x00010000,0x00020000,0x00040000,0x00080000,
	0x00100000,0x00200000,0x00400000,0x00800000,
	0x00800000,0x00400000,0x00200000,0x00100000,
	0x00080000,0x00040000,0x00020000,0x00010000,
	0x00010000,0x00020000,0x00040000,0x00080000,
	0x00100000,0x00200000,0x00400000,0x00800000,
	0x00180000,0x00240000,0x00420000,0x00810000,
	0x00180000,0x00240000,0x00420000,0x00810000,
	0x00180000,0x00240000,0x00420000,0x00810000,
	0x00180000,0x00240000,0x00420000,0x00810000,
	0x00810000,0x00420000,0x00240000,0x00180000,
	0x00810000,0x00420000,0x00240000,0x00180000,
	0x00810000,0x00420000,0x00240000,0x00180000,
	0x00810000,0x00420000,0x00240000,0x00180000
};
int main()
{
	int k;
	PINSEL2=0x00000000;
	IO1DIR=0x00ff0000;
	for(k=0;1;k++)
	{
		if(k>63)
		{
			k=0;
		}
		IO1CLR=0xffffffff;
		IO1SET=~pattern[k];
		delay(500);
	}
}
void delay(int a)
{
	unsigned int i,j;
	for(i=0;i<a;i++)
	{
		for(j=0;j<100;j++);
	}
}